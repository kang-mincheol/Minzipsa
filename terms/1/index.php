제7조 (페이서비스 지급기준)<br>
1. 페이서비스 선정산금 한도는 회원이 거래처로부터 지급받을 것으로 예상되는 정산금을 최대한도로 하며, 회사는 회원에 대한 심사 후 최종한도를 부여합니다.<br>
2. 페이서비스 선정산금은 회원이 페이서비스를 통해 거래를 신청하고 심사 및 계약체결 등 회사의 제반 절차가 완료된 이후 지급되며, 이때 지급은 회원이 회사에 사전에 등록한 계좌로 지급합니다.<br>
3. 선정산업체가 정산금 채권을 양수한 후 다음 각 호의 사유에 의하여 거래처로부터 정산 받지 못한 금액 및 관련 손해배상금 일체는 미수금액에 해당합니다.<br>
