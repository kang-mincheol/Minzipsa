① 페이서비스: 회원이 온라인쇼핑몰, 판매대행사 등 거래처(이하 “거래처”)에서 통신판매 방식으로 상품을 판매하고 발생한 매출에 대하여, 거래처로부터 상품 판매와 관련한 수수료 등 제비용을 공제하고 받을 나머지 정산금(이하 “정산금”) 채권을, 그 지급 받는 예정 정산시점보다 앞서서 선정산업체에게 양도하고 선정산금을 미리 지급 받는 데일리페이 서비스를 말합니다.<br>
