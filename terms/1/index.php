제 4 조 (회원 가입)<br>
1. 회원은 회사가 정한 회원가입 양식에 따라 회원정보를 기입한 후 이 약관에 동의한다는 의사표시를 함으로서 회원가입을 신청합니다.<br>
2. 회사는 일부 사이트의 특정 서비스를 제공하기 위해 회원에게 별도 또는 추가적인 가입절차를 요청할 수 있으며, 이러한 특정 서비스를 이용할 경우 해당 사이트 또는 서비스에 대한 이용약관, 규정 또는 세칙 등이 이 약관보다 우선 적용됩니다.<br>
3. 회사는 회원이 페이서비스를 쉽게 이용할 수 있도록 회원의 아이디를 포함한 회원정보를 관리하거나 인터넷 사이트 또는 서비스를 개선, 변경할 수 있습니다.<br>
4. 회원은 회원가입 시에 회사가 페이서비스를 제공함에 있어서 필요한 정보를 제공해야 하며, 회사가 그 필요에 따라 별도로 요청할 경우 요청 받은 서류를 제출해야 합니다.<br>
5. 회원이 사실과 다른 정보 또는 허위 정보를 기입하거나 추후 그러한 정보임이 밝혀질 경우 운영자의 권한으로 서비스 이용을 일시 정지하거나 영구정지 또는 이용 계약을 해지할 수 있습니다. 이로 인하여 회사 또는 제3자에게 발생한 손해는 해당 회원이 책임을 집니다. 다만, 회사의 고의나 과실에 의하여 손해가 발생한 경우에는 회사가 손해를 부담합니다.<br>
6. 회사는 회원이 가입 신청한 경우 법령에 따라 이용 가능한 신용정보업자 또는 신용정보집중기관을 통하여 회원 정보 진위 여부를 확인할 수 있으며, 회사는 진위 여부를 확인할 수 없는 이용신청에 대해서는 신청자에게 증빙자료를 요청할 수 있습니다.<br>
7. 회사는 그 판단에 따라 가입 신청에 대한 승낙을 유보할 수 있습니다. 이 경우, 회사는 가입 신청자에게 승낙유보의 사유, 승낙가능시기 또는 승낙에 필요한 추가요청정보 내지 자료 등 기타 승낙유보와 관련된 사항을 해당 서비스 화면에 게시하거나 기타 방법을 통하여 가입 신청자에게 통지합니다.<br>
