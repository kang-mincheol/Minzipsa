제 5 조 (페이서비스의 내용)<br>
1. 회원은 거래처에 입점하여 판매한 상품판매대금과 관련한 정산금 채권을 선정산업체에 양도하고, 페이서비스를 통해 선정산금을 미리 지급받게 됩니다.<br>
2. 회사 및 선정산업체는 회원과 관련된 구체적인 권리, 의무를 정하기 위하여 이 약관과는 별도로 “정산금채권 등 양수도계약서”, “서비스 신청서” 및 기타 개별계약(이하 총칭하여 “개별계약”)을 체결할 수 있습니다.<br>
3. 선정산업체는 페이서비스를 통해 회원의 정산금 채권을 양수하고 선정산금을 지급한 뒤, 양수한 정산금 채권에 기하여 거래처로부터 선정산업체가 지정한 계좌(가상계좌 포함, 이하 동일)로 정산금을 직접 지급받습니다. 이때 선정산업체는 선정산금의 자금 조달을 위하여 회원으로부터 양수한 정산금 채권을 제3자에게 담보 목적으로 양도할 수 있습니다.<br>
4. 선정산업체가 전항에 따라 거래처로부터 선정산업체 지정 계좌로 직접 지급받은 정산금 금액(입금 사유를 불문하며, 동 계좌로 입금된 일체의 금원을 말함)은 서비스 이용료, 기 지급한 선정산금 순으로 변제충당되며, 회원은 이에 대해 동의합니다.<br>
5. 선정산업체에 초과정산금이 발생한 경우, 선정산업체는 정산금 수령일로부터 2영업일 이내에 해당 초과정산금을 회원이 회사에 사전에 등록한 계좌로 지급합니다. 만약 미수금액이 발생하는 등의 사유로 정산금이 “선정산금 잔액과 서비스 이용료의 합계액”에 미달하는 경우, 그 차액은 차회차 정산금에서 우선적으로 충당하게 됩니다.<br>
6. 회사는 회원에게 페이서비스를 통한 지급내용 및 기타 거래에 관한 제반 사항을 회사 홈페이지 또는 기타 장치를 이용해 명시하고 회원이 서비스 및 거래에 관한 모든 정보를 알 수 있도록 그 내용을 제공하여야 합니다. 단 기밀을 요하거나 회사 내부에 관련된 정보는 제외됩니다.<br>
