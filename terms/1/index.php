3. 회사가 이 약관을 개정하는 경우에는 개정된 약관의 적용일자 및 개정사유를 명시하여 그 적용일자 7일 이전부터 적용일자 전일까지 위 1항의 방법으로 공지함으로써, 그 효력이 발생합니다. 단, 회원의 권리나 의무에 중대한 영향을 주는 변경이 아닌 경우에는 적용일자 전일까지 공지하도록 합니다.<br>
4. 이 약관은 회사와 회원간에 성립되는 페이서비스 이용계약의 기본약정입니다. 회사는 필요한 경우 특정 서비스에 관하여 적용될 사항(이하 “개별약관”)을 정하여 미리 공지할 수 있습니다. 회원이 이러한 개별약관에 동의하고 특정 서비스를 이용하는 경우에는 개별약관이 우선적으로 적용되고, 이 약관은 보충적인 효력을 갖습니다. 개별약관의 변경에 관해서는 개별약관에 특별한 규정이 없는 한 위 3항을 준용합니다.<br>
