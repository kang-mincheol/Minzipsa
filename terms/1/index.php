제 1 조 (목적)<br>
이 약관은 주식회사 데일리펀딩(이하 “회사”)이 인터넷 사이트(www.daily-pay.co.kr) 및 모바일 어플리케이션 등을 통하여 제공하는 데일리페이 서비스와 관련하여 회사와 회원 간의 권리와 의무, 책임사항 및 회원의 서비스 이용절차에 관한 사항을 규정함을 목적으로 합니다.<br>
